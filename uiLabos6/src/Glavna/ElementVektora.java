package Glavna;

public class ElementVektora {
	 private int frekvencija;
	 private String rijec;
	
	 
	 public ElementVektora(int frekvencija, String rijec) {
		super();
		this.frekvencija = frekvencija;
		this.rijec = rijec;
	}


	public int getFrekvencija() {
		return frekvencija;
	}


	public void setFrekvencija(int frekvencija) {
		this.frekvencija = frekvencija;
	}


	public String getRijec() {
		return rijec;
	}


	public void setRijec(String rijec) {
		this.rijec = rijec;
	}
	
	 
	 
	 
	 
	 
	 

}
